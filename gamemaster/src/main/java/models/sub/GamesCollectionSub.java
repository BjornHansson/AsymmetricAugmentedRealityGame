package models.sub;

/**
 * Represents a list of games.
 */
public class GamesCollectionSub {
	private int gameId;
	private String name;

	public int getGameId() {
		return gameId;
	}

	public void setGameId(int gameId) {
		this.gameId = gameId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
